package teoria;

public class EstructurasControl 
{

	public static void main(String[] args) 
	{
	    char dia = 'b';
	    switch (dia) { 
		    case 'a': System.out.println("Lunes"); break;
		    case 'b': System.out.println("Martes"); break; 
		    case 'c': System.out.println("Miercoles"); break; 
		    case 'd': System.out.println("Jueves"); break; 
		    case 'e': System.out.println("Viernes"); break; 
		    default: System.out.println("Desconocido"); 
	    }
	    System.out.println("XD");
	  

	}
}
